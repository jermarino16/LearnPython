#regular range(num) example
print("we are going to do a range(7) example")
for x in range(7):
	print(x)

#interval range example
print("we are going to do a range(2, 8) example")
for x in range(2, 8):
	print(x)

#interval with step range example
print("we are going to do a range(1, 10, 2) example")
for x in range(1, 10, 2):
	print(x)

#interval but counting down example
print("we are going to do a range(7, 0, -1) example")
for x in range (7, 0, -1):
	print(x)